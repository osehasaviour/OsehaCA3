public class American_Flag2 {

  public static void main(String[] args) {
    String row = "*****=======";
    int rows = 4;
    for(int i = 0; i < rows; i++){
      System.out.println(row);
    }
    String row2 = "============";
    int rows2 = 4;
    for(int j = 0; j < rows2; j++){
      System.out.println(row2);
      // AMERICAN FLAG USING SINGLE LOOP
    }
  }
}