public class Nigerian_Flag1 {

  public static void main(String[] args) {
    for (int a = 0; a < 5; a++){
      System.out.println();
      for (int b = 0; b< 5; b++){
        System.out.print("*");
      }
      System.out.print(" ");
      for (int c = 0; c< 5; c++){
        System.out.print("=");
      }
      System.out.print(" ");
      for (int d = 0; d< 5; d++){
        System.out.print("*");
        // NIGERIAN FLAG USING NESTED LOOP
      }
    }
  }
}