public class American_Flag1 {

  public static void main(String[] args) {
    for (int a = 0; a < 4; a++){
      System.out.println();
      for (int b = 0; b< 5; b++){
        System.out.print("*");
      }
      for (int c = 0; c< 10; c++){
        System.out.print("=");
      }
    }
    for (int d = 0; d < 3; d++){
      System.out.println();
      for (int e = 0; e < 15; e++){
        System.out.print("=");
        // AMERICAN FLAG USING NESTED LOOP
      }
    }
  }
}